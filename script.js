const menuContainers = {
  cocktails: document.getElementById("cocktails-grid"),
  wines: document.getElementById("wines-grid"),
  sangrias: document.getElementById("sangrias-grid"),
  bites: document.getElementById("bites-grid")
};

const languageButtons = document.querySelectorAll(".lang-btn");
const i18nNodes = document.querySelectorAll("[data-i18n]");

function createMenuCard(item) {
  return `
    <article class="menu-item">
      <div class="menu-item__inner">
        <div class="menu-item__content">
          <h3 class="menu-item__title">${item.name}</h3>
          <p class="menu-item__text">${item.desc}</p>
        </div>
        <div class="menu-item__price">${item.price}</div>
      </div>
    </article>
  `;
}

function renderMenus(lang) {
  const dictionary = translations[lang];
  Object.entries(menuContainers).forEach(([section, container]) => {
    container.innerHTML = dictionary.menu[section].map(createMenuCard).join("");
  });
}

function applyStaticTranslations(lang) {
  const dictionary = translations[lang];
  i18nNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });
}

function updateActiveLanguageButton(lang) {
  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  document.documentElement.lang = lang;
  applyStaticTranslations(lang);
  renderMenus(lang);
  updateActiveLanguageButton(lang);
  localStorage.setItem("roca-negra-language", lang);
}

function detectInitialLanguage() {
  const savedLanguage = localStorage.getItem("roca-negra-language");
  if (savedLanguage && translations[savedLanguage]) return savedLanguage;

  const browserLanguage = navigator.language.slice(0, 2).toLowerCase();
  return translations[browserLanguage] ? browserLanguage : "es";
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.lang);
  });
});

document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", (event) => {
    const targetSelector = anchor.getAttribute("href");
    const target = document.querySelector(targetSelector);

    if (target) {
      event.preventDefault();
      target.scrollIntoView({
        behavior: "smooth",
        block: "start"
      });
    }
  });
});

setLanguage(detectInitialLanguage());
